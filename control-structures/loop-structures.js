// For Loop
// for (start; condition; mutation) {
// ... do something
// }

// the order is
// 1. start
// 2. condition
// 3. do something (execution)
// 4. mutation

for (var i = 0; i < 10; i++) {
  console.log(i);
}

// While Loop
// while (some condition) {
// ... do something
// }
var count = 0;
while (count < 10) {
  console.log(i);
  i++;
}


// Do While Loop
// in this case, the execution happens first, and next the condition verification
var someNumber = 0;
do {
  console.log(someNumber);
  i++;
} while (someNumber < 10)