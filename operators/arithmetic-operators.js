var number = 1;
var anotherNumber = 2;

// Addition / Plus operator
console.log(number + anotherNumber) // will print 3

// Subtraction operator
console.log(number - anotherNumber) // will print -1

// Multiplication operator
console.log(number * anotherNumber) // will print 2

// Division operator
console.log(number / anotherNumber) // will print 0.5

// Remainder / Mudulus operator (first do the extraction, followed by the rest calc)
console.log(number % anotherNumber) // will print 1

// Exponentiation operator
console.log(number ** anotherNumber) // will print 1

// Incremend and decrement will always return the initial value and after do the operation
// Increment operator
console.log(number++) // will print 1 :P

// Decrement operator
console.log(number--) // will print 2 :P
