var number = 1;
var anotherNumber = 2;

// Equal to
console.log(number == anotherNumber); // false

// Equal to (strict)
console.log(number === anotherNumber); // false

// Different from
console.log(number != anotherNumber); // true

// Greater than
console.log(number > anotherNumber); // false

// Greater than or equal to
console.log(number >= anotherNumber); // false

// Less than
console.log(number < anotherNumber); // true

// Less than or equal to
console.log(number <= anotherNumber); // true