// = Assignment
var number = 1;

// += Sum assignment
console.log(number += 3); // number = number + 3

// -= Subtraction assignment
console.log(number -= 3); // number = number - 3

// *= Multiplication assignment
console.log(number *= 3); // number = number * 3

// /= Division assignment
console.log(number /= 3); // number = number / 3

// %= Remainder assignment
console.log(number %= 3); // number = number % 3

